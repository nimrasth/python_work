# conditional tests
car = 'subaru'
print("\nIs car == 'subaru'? I predict True.")
print(car == 'subaru')
print("\nIs car == 'audi'? I predict False.")
print(car == 'audi')

drink = 'coffee'
print("\nIs drink == 'coffee'? I predict True.")
print(drink == 'coffee')
print("\nIs drink == 'tea'? I predict False.")
print(drink == 'tea')

food = 'ramen'
print("\nIs food == 'ramen'? I predict True.")
print(food == 'ramen')
print("\nIs food == 'pasta'? I predict False.")
print(food == 'pasta')

shoes = 'caliber'
print("\nIs shoes != 'caliber'? I predict True.")
